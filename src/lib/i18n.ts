// ── Helpers ────────────────────────────────────────────────────────────────

export function getLang(): LangCode {
  if (typeof window === 'undefined') return 'es';
  return (localStorage.getItem('glovol_lang') as LangCode) || 'es';
}

export function setLang(code: LangCode) {
  if (typeof window === 'undefined') return;
  localStorage.setItem('glovol_lang', code);
  document.documentElement.lang = code;
  const isRtl = LANGUAGES.find(l => l.code === code)?.rtl ?? false;
  document.documentElement.dir = isRtl ? 'rtl' : 'ltr';
  window.dispatchEvent(new CustomEvent('glovol:lang', { detail: code }));
}

export function t(key: TranslationKey, lang?: LangCode, vars?: Record<string, string | number>): string {
  const l = lang || getLang();
  let str = T[l]?.[key] ?? T['es'][key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, String(v));
    });
  }
  return str;
}

// Hook de React
export function useLang() {
  // devuelve el lang actual — se usa con useState en los componentes
  if (typeof window === 'undefined') return 'es' as LangCode;
  return getLang();
}
